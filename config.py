# ==========================================
# 自动化执行延迟配置 (单位：秒)
# ==========================================
# 如果你的电脑在执行快捷键时出现卡顿、剪贴板读取失败、或者达芬奇面板没来得及打开，
# 请在此处适当调大以下延迟时间（例如从 0.15 改为 0.3）。

# ==========================================
# 达芬奇端 (DaVinci Resolve)
# ==========================================
# 【UI 动画弹出缓冲】
# 场景：程序在盲操时会按下 M 键。如果你的电脑/软件较卡，达芬奇的“标记面板”可能还没完全弹出来，程序就急着按下了 Ctrl+C/Ctrl+V，从而导致复制或粘贴失败。
# 症状：如果你发现经常提取不到标题，或者绑定时标题没被替换掉，请调大此值（例如 0.5）。
DELAY_PANEL_ANIMATION = 0.30

# 【快捷键系统吞吐缓冲】
# 场景：程序帮你发出 Ctrl+C（复制）或 Ctrl+V（粘贴）指令后，Windows 操作系统处理剪贴板数据需要一点点微秒级的时间。
# 症状：极其罕见的偶发性提取失败，可适当调大（例如 0.2）。
DELAY_SHORTCUT_EXEC = 0.10

# ==========================================
# Notion 端
# ==========================================
# 【Notion 专属剪贴板写入缓冲】
# 场景：程序在 Notion 里按下原生快捷键 Alt+Shift+L。此时 Notion 需要在云端或本地生成这行文字，然后再塞进你的 Windows 剪贴板，这比普通的 Ctrl+C 要慢得多。
# 症状：如果你发现在 Notion 里按了快捷键，但程序依然弹窗报错“您当前选中的可能不是一个合法的 Notion Block”，请果断调大此值（例如 0.3 或 0.5）。
DELAY_NOTION_CLIPBOARD = 0.2

# 系统全局
# 【防物理按键污染缓冲】
# 场景：当你按住 Alt+Shift+F 触发程序时，为了防止你手指没松开导致程序后续按下的 M 键被系统识别成了 Alt+Shift+M（从而导致达芬奇没反应），
# 程序会在最开始向系统发送强制释放 Alt/Shift 的信号。这个延迟就是留给系统把按键状态“清零”的缓冲时间。
DELAY_ROUTER_MODIFIER_RELEASE = 0.1
